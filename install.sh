cat > "$APP_EXE" <<EOF || die "Could not write the app executable."
#!/bin/bash
# Clean Desktop — $MARKER.
# Runs the cleaner in the background: no window opens. Output is captured to
# the run log; a notification reports the result.
# (To change behavior, edit the repo instead of this file, then re-run install.sh.)
notify() {
  osascript -e "display notification \"\$1\" with title \"Desktop Cleaner\"" >/dev/null 2>&1 || true
}
DEST="\${DESKTOP_CLEANER_DEST:-\$HOME/Documents/Desktop}"
LOG_DIR="\$DEST"
mkdir -p "\$LOG_DIR" 2>/dev/null || LOG_DIR="\${TMPDIR:-/tmp}"
SCRIPT="$SCRIPT"
if [ ! -r "\$SCRIPT" ]; then
  # Repo moved/deleted, or macOS is blocking access to where it lives.
  notify "Can't read the Desktop Cleaner repo — moved, or access blocked? Re-run install.sh. Nothing moved."
  exit 1
fi
exec "\$SCRIPT" > "\$LOG_DIR/.last-run.txt" 2>&1
EOF

chmod +x "$APP_EXE" || die "Could not make the app executable runnable."
# Best-effort ad-hoc signature, and clear any quarantine flag so Gatekeeper
# won't block the double-click.
codesign --force -s - "$APP" >/dev/null 2>&1 || true
xattr -dr com.apple.quarantine "$APP" 2>/dev/null || true

