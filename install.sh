#!/bin/bash
#
# Desktop Cleaner — installer
# Builds a double-clickable "Clean Desktop.app" on your Desktop. Unlike a
# .command file, an app bundle runs its script directly — no Terminal window
# opens and nothing needs to be closed. The run happens silently in the
# background; a macOS notification reports the result, and full output is
# written to the run log.
#
# The app is a thin wrapper around bin/clean-desktop.sh in this repo, so edits
# here take effect immediately. If you *move this repo*, re-run ./install.sh.

set -u

REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SCRIPT="$REPO_DIR/bin/clean-desktop.sh"
DESKTOP="${DESKTOP_CLEANER_DESKTOP:-$HOME/Desktop}"
APP_NAME="Clean Desktop.app"
APP="$DESKTOP/$APP_NAME"
APP_EXE="$APP/Contents/MacOS/Clean Desktop"
SERVICES_DIR="${DESKTOP_CLEANER_SERVICES_DIR:-$HOME/Library/Services}"
WORKFLOW="$SERVICES_DIR/Clean Desktop.workflow"
MARKER="generated by Desktop Cleaner install.sh"
DEST_SHOW="${DESKTOP_CLEANER_DEST:-$HOME/Documents/Desktop}"

die() { echo "❌ $1"; exit 1; }

[ -f "$SCRIPT" ] || die "Cannot find $SCRIPT — run this from inside the repo."

chmod +x "$SCRIPT" || die "Could not make $SCRIPT executable."

# Replace a previously generated app; refuse to clobber anything we didn't make.
# -L catches a dangling symlink, which -e alone would miss.
if [ -e "$APP" ] || [ -L "$APP" ]; then
  if grep -q "$MARKER" "$APP_EXE" 2>/dev/null; then
    rm -rf "$APP"
  else
    die "$APP already exists and was not $MARKER — move it aside and re-run."
  fi
fi

# Retire the old Terminal-based launcher, if a previous version installed one.
OLD_LAUNCHER="$DESKTOP/Clean Desktop.command"
if [ -f "$OLD_LAUNCHER" ] && grep -q "$MARKER" "$OLD_LAUNCHER" 2>/dev/null; then
  rm -f "$OLD_LAUNCHER"
  echo "🧹 Removed old Terminal-based launcher: $OLD_LAUNCHER"
fi

mkdir -p "$APP/Contents/MacOS" || die "Could not create $APP — is the Desktop writable?"

cat > "$APP/Contents/Info.plist" <<'EOF' || die "Could not write Info.plist."
